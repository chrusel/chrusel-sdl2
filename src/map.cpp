Map::Map()
{
    m_dirt = TextureManager::LoadTexture("/home/g041840/sdl2-chrusel/assets/dirt.png");
    m_grass = TextureManager::LoadTexture("/home/g041840/sdl2-chrusel/assets/grass.png");
    m_water = TextureManager::LoadTexture("assets/water.png");

    loadMap(lvl1);

    m_src.x = 0;
    m_src.y = 0;
    m_src.w = 32;
    m_src.h = 32;

    m_dest.x = 0;
    m_dest.y = 0;
    m_dest.w = 32;
    m_dest.h = 32;
}

Map::~Map()
{}

void Map::loadMap(int arr[20][25]) {
    for (int row = 0; row < 20; row++) {
        for (int column = 0; column < 25; column++) {
            m_map[row][column] = arr[row][column];
        }
    }
}

void Map::drawMap()
{
    int type = 0;
    for (int row = 0; row < 20; row++) {
        for (int column = 0; column < 25; column++) {
            type = m_map[row][column];
            m_dest.x = column * 32;
            m_dest.y = row * 32;

            switch (type)
            {
            case 0:
                TextureManager::draw(m_water, m_src, m_dest);
                break;
            case 1:
                TextureManager::draw(m_grass, m_src, m_dest);
                break;
            case 2:
                TextureManager::draw(m_dirt, m_src, m_dest);
                break;

            default:
                TextureManager::draw(m_water, m_src, m_dest);
                break;
            }
        }
    }
}